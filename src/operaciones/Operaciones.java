package operaciones;

public class Operaciones {
    
    public static int sumar(int a, int b){
        System.out.println("resultado = a + b entero");
        return a + b;
    }
    
    public static double sumar(double a, double b){
        System.out.println("resultado 2 = a + b double");
        return a + b;
    }
}
